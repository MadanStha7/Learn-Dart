void main() {
  // The var keyword (normal variable where data type not needed to mentioned)
  var age = 10;

  // The final keyword (cannot be changed later/ read only variable)
  final name = "Peter";
  // name = "Shretha";

  //The const keyword (same as final keyword/ a compile time constant)
  const lastName = "Stha";

  //The dynamic keyword (data tye of variable can be changed.)
  dynamic temp = "10";
  temp = 10;
}

